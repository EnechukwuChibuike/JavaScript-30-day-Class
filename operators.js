// Greater than

console.log(3 > 2);

// Less than

console.log(3 < 3);

// Greater than or Equal to

console.log(3 >= 2);

// Less than or Equal to

console.log(3 <= 2);

// Equal to

console.log(2 == 2);

// Equal type and value

console.log("2" === 2);

// Not Equal to

console.log(3 != 3);

// Not equal type and value

console.log("2" !== 2);

// ...........................LOGICAL OPERATORS.........................

// AND

console.log(2 == 2 && 3 == 4);
console.log(2 == 2 && 3 == 3);

// OR

console.log(2 == 2 || 3 == 4);
console.log(2 == 2 || 3 == 3);

// NOT

let number;

console.log(!number);
